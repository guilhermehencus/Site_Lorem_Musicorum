        <h2 id="topo">Noções Básicas: divisão de tempo</h2>
        <!-- Conteudo -->
        <div class="texto">
            <p>
                Para começarmos a ler, primeiro precisamos entender o que cada elemento de uma partitura quer nos
                dizer, caso contrário a música não será executada da mesma forma que foi escrita, logo a mensagem que
                ela deveria passar não será enviada ou será distoricida e os ouvintes terão uma impressão errada.
            </p>

            <p>
                Por isso é importante que o instrumentista e/ou o cantor saiba interpretar uma partitura e cometa
                o mínimo de erros possível durante a leitura.
            </p>

            <p>
                O objetivo não é colocar uma partitura inteira para leitura, mas sim criar noções antes disso
                para que depois possamos aplicar a uma música real. Estaremos começando, por agora, a trabalhar com a
                <b><a href="introducao.php#duracao">duração</a></b> de um som, independente de qual seja, e vamos pensar
                por enquanto em segundos.
            </p>

            <p>
                Vamos aos exercicios, logo abaixo para poder praticar:
            </p>

            <h3 id="tempo">Noção de tempo</h3>

            <p>
                Desde criança nós sabemos como as horas funcionam: 1 hora é composta por 60 minutos, que por
                sua vez é composta por 60 segundos. Então vamos brincar, agora!
            </p>

            <h3>Exercicios</h3>

            <p>
                Exercicio 1.<br />
                <br /> Com um relógio analógico (ou digital em que possa ver os segundos mudarem) em mãos,
                pratique dizer a palavra "pão" a cada 2 segundo(s), sincronizando sua fala com o contador de segundos
                do relógio. <hr />
            </p>

            <p>
                Exercicio 2.<br />
                <br /> Com um relógio analógico (ou digital em que possa ver os segundos mudarem) em mãos,
                pratique dizer a palavra "pão" a cada 1 segundo(s), sincronizando sua fala com o contador de segundos
                do relógio. <hr />
            </p>

            <p>
                Exercicio 3.<br />
                <br /> Com um relógio analógico (ou digital em que possa ver os segundos mudarem) em mãos,
                pratique dizer a palavra "pão" a cada 1/2 segundo(s), sincronizando sua fala com o contador de segundos
                do relógio. <hr />
            </p>

            <p>
                Casos tenha dificuldades neste inicio, não se apavore. Tudo é questão de prática. Caso sinta muita
